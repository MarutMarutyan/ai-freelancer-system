"""Целевые категории Kwork для сканирования."""

# Категории сгруппированы по фазам развития профиля.
# Фаза 1: тексты и переводы (простые заказы для набора отзывов)
# Фаза 2: веб-разработка (лендинги, доработки)
# Фаза 3: скрипты, боты, автоматизация

# Формат: {category_id: (название, фаза)}
CATEGORIES = {
    # === Фаза 1: Тексты и переводы ===
    "5": ("Тексты и переводы", 1),
    "35": ("Переводы", 1),
    "74": ("Продающие тексты", 1),
    "73": ("Наполнение сайта", 1),

    # === Фаза 2: Веб-разработка ===
    "11": ("Разработка и IT", 2),
    "37": ("Веб-разработка", 2),
    "38": ("Доработка сайта", 2),

    # === Фаза 3: Скрипты и боты ===
    "41": ("Скрипты и боты", 3),
}


def get_categories_for_phase(phase: int) -> list[str]:
    """Получить ID категорий для указанной фазы и всех предыдущих."""
    return [
        cat_id
        for cat_id, (_, cat_phase) in CATEGORIES.items()
        if cat_phase <= phase
    ]


def get_category_name(category_id: str) -> str:
    """Получить название категории по ID."""
    if category_id in CATEGORIES:
        return CATEGORIES[category_id][0]
    return f"Категория #{category_id}"


# Текущая фаза (начинаем с 1 — тексты)
CURRENT_PHASE = 1

# Активные категории для сканирования
ACTIVE_CATEGORIES = get_categories_for_phase(CURRENT_PHASE)
